/*


1)var
Scope: var ka scope function-level hota hai. Matlab agar ek function ke andar declare kiya, to bas usi function ke andar kaam karega.
Re-declare: Ek hi scope me same naam ka variable dobara declare kar sakte ho.
Hoisting: var hoist hota hai. Matlab code chalne se pehle declare hota hai, par value undefined hoti hai.
Use: Purane JavaScript me zyada use hota tha, ab zyada use mat karo.



2) let
Scope: let ka scope block-level hota hai. Matlab {} ke andar hi kaam karega.
Re-declare: Same block me dobara declare nahi kar sakte.
Hoisting: let hoist hota hai, lekin jab tak value assign na karo, tab tak use nahi kar sakte (Temporal Dead Zone).
Use: Modern JavaScript (ES6) me mostly let use hota hai.



3) const
Scope: const bhi block-level hota hai, bilkul let ki tarah.
Re-declare: Dobara declare nahi kar sakte, aur value ko re-assign bhi nahi kar sakte.
Hoisting: const bhi hoist hota hai, lekin bina initialize kare use nahi kar sakte (Temporal Dead Zone).
Use: Jab ek value kabhi change nahi honi chahiye, tab const use karo.


*/

